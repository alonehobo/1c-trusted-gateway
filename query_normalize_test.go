package main

import "testing"

func TestNormalizeQueryAliases(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  string
	}{
		// === Single query ===
		{
			name:  "dotted field with wrong alias",
			input: "ВЫБРАТЬ Номенклатура.Код КАК КодТовара ИЗ Справочник.Номенклатура КАК Номенклатура",
			want:  "ВЫБРАТЬ Номенклатура.Код КАК НоменклатураКод ИЗ Справочник.Номенклатура КАК Номенклатура",
		},
		{
			name:  "simple field with wrong alias",
			input: "ВЫБРАТЬ Наименование КАК Имя ИЗ Справочник.Валюты",
			want:  "ВЫБРАТЬ Наименование КАК Наименование ИЗ Справочник.Валюты",
		},
		{
			name:  "already correct — unchanged",
			input: "ВЫБРАТЬ Номенклатура.Код КАК НоменклатураКод ИЗ Справочник.Номенклатура КАК Номенклатура",
			want:  "ВЫБРАТЬ Номенклатура.Код КАК НоменклатураКод ИЗ Справочник.Номенклатура КАК Номенклатура",
		},
		{
			name:  "function",
			input: "ВЫБРАТЬ МАКСИМУМ(Контрагент.Наименование) КАК Поле ИЗ Справочник.Контрагенты КАК Контрагент",
			want:  "ВЫБРАТЬ МАКСИМУМ(Контрагент.Наименование) КАК МАКСИМУМКонтрагентНаименование ИЗ Справочник.Контрагенты КАК Контрагент",
		},
		{
			name:  "nested function",
			input: "ВЫБРАТЬ КОЛИЧЕСТВО(РАЗЛИЧНЫЕ Контрагент.Ссылка) КАК Кол ИЗ Документ.Реализация КАК Р",
			want:  "ВЫБРАТЬ КОЛИЧЕСТВО(РАЗЛИЧНЫЕ Контрагент.Ссылка) КАК КОЛИЧЕСТВОРАЗЛИЧНЫЕКонтрагентСсылка ИЗ Документ.Реализация КАК Р",
		},
		{
			name:  "no aliases — unchanged",
			input: "ВЫБРАТЬ Наименование ИЗ Справочник.Номенклатура",
			want:  "ВЫБРАТЬ Наименование ИЗ Справочник.Номенклатура",
		},

		// === ВЫРАЗИТЬ / CAST — КАК inside parens must be preserved ===
		{
			name:  "ВЫРАЗИТЬ — КАК inside parens not touched",
			input: "ВЫБРАТЬ ВЫРАЗИТЬ(Контрагент.Наименование КАК Строка(100)) КАК Поле ИЗ Справочник.Контрагенты КАК К",
			want:  "ВЫБРАТЬ ВЫРАЗИТЬ(Контрагент.Наименование КАК Строка(100)) КАК ВЫРАЗИТЬКонтрагентНаименованиеСтрока100 ИЗ Справочник.Контрагенты КАК К",
		},
		{
			name:  "ВЫРАЗИТЬ simple field",
			input: "ВЫБРАТЬ ВЫРАЗИТЬ(Сумма КАК Число(15, 2)) КАК СуммаЧисло ИЗ Документ.Реализация",
			want:  "ВЫБРАТЬ ВЫРАЗИТЬ(Сумма КАК Число(15, 2)) КАК ВЫРАЗИТЬСуммаЧисло152 ИЗ Документ.Реализация",
		},

		// === ОБЪЕДИНИТЬ / UNION ===
		{
			name: "ОБЪЕДИНИТЬ — both branches normalized",
			input: "ВЫБРАТЬ Код КАК Код ИЗ Справочник.Валюты " +
				"ОБЪЕДИНИТЬ " +
				"ВЫБРАТЬ Контрагент.Наименование КАК Код ИЗ Справочник.Контрагенты КАК Контрагент",
			want: "ВЫБРАТЬ Код КАК Код ИЗ Справочник.Валюты " +
				"ОБЪЕДИНИТЬ " +
				"ВЫБРАТЬ Контрагент.Наименование КАК КонтрагентНаименование ИЗ Справочник.Контрагенты КАК Контрагент",
		},
		{
			name: "ОБЪЕДИНИТЬ ВСЕ",
			input: "ВЫБРАТЬ Наименование КАК Имя ИЗ Справочник.Валюты " +
				"ОБЪЕДИНИТЬ ВСЕ " +
				"ВЫБРАТЬ Контрагент.Наименование КАК Имя ИЗ Справочник.Контрагенты КАК Контрагент",
			want: "ВЫБРАТЬ Наименование КАК Наименование ИЗ Справочник.Валюты " +
				"ОБЪЕДИНИТЬ ВСЕ " +
				"ВЫБРАТЬ Контрагент.Наименование КАК КонтрагентНаименование ИЗ Справочник.Контрагенты КАК Контрагент",
		},
		{
			name: "UNION ALL english",
			input: "SELECT Name AS N FROM Catalog.Currencies " +
				"UNION ALL " +
				"SELECT Partner.Name AS N FROM Catalog.Partners AS Partner",
			want: "SELECT Name AS Name FROM Catalog.Currencies " +
				"UNION ALL " +
				"SELECT Partner.Name AS PartnerName FROM Catalog.Partners AS Partner",
		},
		{
			name: "three-way ОБЪЕДИНИТЬ",
			input: "ВЫБРАТЬ А КАК Х ИЗ Т1 " +
				"ОБЪЕДИНИТЬ ВЫБРАТЬ Б КАК Х ИЗ Т2 " +
				"ОБЪЕДИНИТЬ ВЫБРАТЬ В КАК Х ИЗ Т3",
			want: "ВЫБРАТЬ А КАК А ИЗ Т1 " +
				"ОБЪЕДИНИТЬ ВЫБРАТЬ Б КАК Б ИЗ Т2 " +
				"ОБЪЕДИНИТЬ ВЫБРАТЬ В КАК В ИЗ Т3",
		},

		// === Batch queries (;) ===
		{
			name: "batch: renames propagated",
			input: "ВЫБРАТЬ Валюты.Код КАК ЦифровойКод ПОМЕСТИТЬ ВТ ИЗ Справочник.Валюты КАК Валюты" +
				";ВЫБРАТЬ ВТ.ЦифровойКод КАК Код ИЗ ВТ КАК ВТ",
			want: "ВЫБРАТЬ Валюты.Код КАК ВалютыКод ПОМЕСТИТЬ ВТ ИЗ Справочник.Валюты КАК Валюты" +
				";ВЫБРАТЬ ВТ.ВалютыКод КАК ВТВалютыКод ИЗ ВТ КАК ВТ",
		},
		{
			name: "batch: three queries — renames chain",
			input: "ВЫБРАТЬ Х КАК А ПОМЕСТИТЬ ВТ1 ИЗ Т КАК Т" +
				";ВЫБРАТЬ ВТ1.А КАК Б ПОМЕСТИТЬ ВТ2 ИЗ ВТ1" +
				";ВЫБРАТЬ ВТ2.Б КАК Результат ИЗ ВТ2 КАК ВТ2",
			want: "ВЫБРАТЬ Х КАК Х ПОМЕСТИТЬ ВТ1 ИЗ Т КАК Т" +
				";ВЫБРАТЬ ВТ1.Х КАК ВТ1Х ПОМЕСТИТЬ ВТ2 ИЗ ВТ1" +
				";ВЫБРАТЬ ВТ2.ВТ1Х КАК ВТ2ВТ1Х ИЗ ВТ2 КАК ВТ2",
		},
		{
			name: "security: bypass via temp table — blocked",
			input: "ВЫБРАТЬ Контрагент.Наименование КАК Статус ПОМЕСТИТЬ ВТ ИЗ Справочник.Контрагенты КАК Контрагент" +
				";ВЫБРАТЬ ВТ.Статус КАК Статус ИЗ ВТ КАК ВТ",
			want: "ВЫБРАТЬ Контрагент.Наименование КАК КонтрагентНаименование ПОМЕСТИТЬ ВТ ИЗ Справочник.Контрагенты КАК Контрагент" +
				";ВЫБРАТЬ ВТ.КонтрагентНаименование КАК ВТКонтрагентНаименование ИЗ ВТ КАК ВТ",
		},
		{
			name: "security: bypass via temp table no alias — blocked",
			input: "ВЫБРАТЬ Контрагент.Наименование КАК Статус ПОМЕСТИТЬ ВТ ИЗ Справочник.Контрагенты КАК Контрагент" +
				";ВЫБРАТЬ ВТ.Статус ИЗ ВТ КАК ВТ",
			want: "ВЫБРАТЬ Контрагент.Наименование КАК КонтрагентНаименование ПОМЕСТИТЬ ВТ ИЗ Справочник.Контрагенты КАК Контрагент" +
				";ВЫБРАТЬ ВТ.КонтрагентНаименование ИЗ ВТ КАК ВТ",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := normalizeQueryAliases(tt.input)
			if got != tt.want {
				t.Errorf("\n  got:  %s\n  want: %s", got, tt.want)
			}
		})
	}
}
